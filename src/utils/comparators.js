export function sortNumbersAsc(a, b) {
    /*
        Compare function. Sorts numbers ASC
        usage: your_array.sort(sortNumbersAsc)
    */
    return a - b;
} 

export function sortNumbersDesc(a, b) {
    /*
        Compare function. Sorts numbers DESC
        usage: your_array.sort(sortNumbersDesc)
    */
    return b - a;
}